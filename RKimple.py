def setup_LP(solver,rkm,O,rhs,s):
    """
    This method sets up the necesarry variables for the LP Problem
    
    Parameters:
    solver:     solver that should be used
    rkm:        RKM used
    O:          Order condition matrix
    s:          Number od stages
    
    returns:
    LP_vars Tuple with variables for the LP solver
    
    """
    
    if solver == 'scipy_ip' or solver == 'scipy_sim':
        print('scipy')
        bounds = (0, None)
        A_eq = np.concatenate((O,-O),axis = 1)
        b_eq = rhs - O@rkm.b
            
        e = np.ones(2*s)
        
        LP_vars = (A_eq,b_eq,e,bounds)
            
    else:
        print('cp')
        ap_op =cp.Variable(s)
        an_op =cp.Variable(s)
        e = np.ones(s) #vector for goal Function, just generates the 1-Norm of b
        
        LP_vars = (ap_op,an_op,e,O,rhs)

    return LP_vars

def solve_LP(solver,LP_vars,rkm,u,K,dt,reduce = False,verbose_LP = False,maxval=None, **options):
    """
    This method solves the LP Problem
    
    Parameters:
    
    solver:     a string with the solver that should be used
    LP_vars:    the set of variables generated by setup_LP
    
    reduce:     If set, the LP problem is first solved with a reduced set of constriants
    verbose_LP: prints additional messages
    maxval:     If scalar enforce the value as maximum on solution. If not used set to None
    options:    additional Options, are passed through to the used solver
    
    Returns:
    status: integer representing the status of the algorithm.
      For scipy
        0 : Optimization proceeding nominally.
        1 : Iteration limit reached.
        2 : Problem appears to be infeasible.
        3 : Problem appears to be unbounded.
        4 : Numerical difficulties encountered.
      For cvypy
        0 : Optimization proceeding nominally.
        2 : Problem appears to be infeasible.
        4 : Numerical difficulties encountered.
        5 : solver crashed
        6 : Trivial Problem

    b: found b, if solver failed rkm.b
    
    """

    


                
    if solver == 'scipy_ip' or solver == 'scipy_sim':
        (A_eq,b_eq,e,bounds) = LP_vars
        s = len(rkm.b)
        if solver == 'scipy_ip':
            method = 'interior-point'
        elif solver == 'scipy_sim':
            method = 'revised simplex'
        
        
        if reduce:
            u_ = u +dt*K@rkm.b
            if maxval:
                i = (u_<0)|(u_>maxval)
            else:
                i = (u_<0)
            l = [] #List to store number of positifity constraints considered in LP-Problem
            if not np.any(i):
                #apparently we got the trivial problem that is already ok
                print('trival Problem for LP-Solver')
                return (6,rkm.b,[0])
            while not maxval and np.any(u_<0) or maxval and np.any((u_<0) | (u_>maxval)):
                if maxval:
                    i = (u_<0)|(u_>maxval)|i #update indecies for conditions
                else:
                    i = (u_<0)|i #update indecies for conditions
                if verbose_LP: print(np.sum(i),'constraints')
                l.append(np.sum(i))
                
                u_slice = u[i] #slice the u and K
                K_slice = K[i,:]
                
                if maxval:
                    A_ub_ = np.concatenate((-K_slice,K_slice),axis = 1)
                    A_ub = np.concatenate((A_ub_,-A_ub_),axis = 0)
                    b_ub_1 = 1/dt*u_slice+K_slice@rkm.b  
                    b_ub_2 = 1/dt*(maxval-u_slice)-K_slice@rkm.b
                    b_ub = np.concatenate((b_ub_1,b_ub_2),axis = 0)
                else:
                    A_ub = np.concatenate((-K_slice,K_slice),axis = 1)
                    b_ub = 1/dt*u_slice+K_slice@rkm.b  
                
                res = linprog(e, A_ub=A_ub, b_ub=b_ub,A_eq=A_eq, b_eq=b_eq, bounds=bounds, method=method,
                              options = options)
                
                b = rkm.b+res.x[:s]-res.x[s:]
                u_ = u+dt*K@b 
                
                if (not maxval and np.any(u_<0) and np.all((u_<0)|i == i) 
                    or maxval and np.any((u_<0)|(u_>maxval)) and np.all((u_<0)|(u_>maxval)|i == i)): # there are no new conditions
                    if verbose_LP: print('number of conditions is not increasing')
                    break
      
        else:
            l = [len(u)]
            if maxval:
                    A_ub_ = np.concatenate((-K,K),axis = 1)
                    A_ub = np.concatenate((A_ub_,-A_ub_),axis = 0)
                    b_ub_1 = 1/dt*u+K@rkm.b  
                    b_ub_2 = 1/dt*(maxval-u)-K@rkm.b
                    b_ub = np.concatenate((b_ub_1,b_ub_2),axis = 0)
            else:
                    A_ub = np.concatenate((-K,K),axis = 1)
                    b_ub = 1/dt*u+K@rkm.b    
            res = linprog(e, A_ub=A_ub, b_ub=b_ub,A_eq=A_eq, b_eq=b_eq, bounds=bounds, method=method,options = options)
        
        
        if res.success:
            b = rkm.b+res.x[:s]-res.x[s:]
            status = 0
        else:
            print('solver did not find solution')
            print('solver_state',res.status)
            status = res.status
            b = rkm.b
            
                    
                    
    else:
        (ap_op,an_op,e,O,rhs) = LP_vars
        
        if reduce:
            u_ = u +dt*K@rkm.b
            if maxval:
                i = (u_<0)|(u_>maxval)
            else:
                i = (u_<0)
            l = [] #List to store number of positifity constraints considered in LP-Problem
            if not np.any(i):
                #apparently we got the trivial problem that is already ok
                print('trival Problem for LP-Solver')
                return (6,rkm.b,[0])
            while not maxval and np.any(u_<0) or maxval and np.any((u_<0)| (u_>maxval)):
                if maxval:
                    i = (u_<0)|(u_>maxval)|i #update indecies for conditions
                else:
                    i = (u_<0)|i #update indecies for conditions
                if verbose_LP: print(np.sum(i),'constraints')
                l.append(np.sum(i))
                
                u_slice = u[i] #slice the u and K
                K_slice = K[i,:]
                
                #solve problem for slices u and K
                if maxval:
                    prob = cp.Problem(cp.Minimize(e@ap_op+e@an_op),
                        [O@(ap_op-an_op+rkm.b)==rhs,u_slice+dt*K_slice@(ap_op-an_op+rkm.b)>=0, u_slice+dt*K_slice@(ap_op-an_op+rkm.b)<=maxval,ap_op>=0,an_op>=0]) 
                else:
                    prob = cp.Problem(cp.Minimize(e@ap_op+e@an_op),
                        [O@(ap_op-an_op+rkm.b)==rhs,u_slice+dt*K_slice@(ap_op-an_op+rkm.b)>=0,ap_op>=0,an_op>=0]) 
                try:
                    prob.solve(solver=solver,**options)
                    b = ap_op.value - an_op.value + rkm.b
                except:
                    print('Solver crashed, Replaced b with original b')
                    b = rkm.b
                    status = 5

                u_ = u+dt*K@b
                
                if (not maxval and np.any(u_<0) and np.all((u_<0)|i == i) 
                    or maxval and np.any((u_<0)|(u_>maxval)) and np.all((u_<0)|(u_>maxval)|i == i)): # there are no new conditions
                    if verbose_LP: print('number of conditions is not increasing')
                    break
                      
        else:
            l = [len(u)]
            if maxval:
                prob = cp.Problem(cp.Minimize(e@ap_op+e@an_op),
                    [O@(ap_op-an_op+rkm.b)==rhs,u+dt*K@(ap_op-an_op+rkm.b)>=0,u+dt*K@(ap_op-an_op+rkm.b)<=0,ap_op>=0,an_op>=0]) 
            else:
                prob = cp.Problem(cp.Minimize(e@ap_op+e@an_op),
                    [O@(ap_op-an_op+rkm.b)==rhs,u+dt*K@(ap_op-an_op+rkm.b)>=0,ap_op>=0,an_op>=0]) 
            try:
                prob.solve(solver=solver,**options)
            except:
                print('Solver crashed, Replaced b with original b')
                b = rkm.b
                status = 5
                
        if prob.status == cp.OPTIMAL:
            b = ap_op.value - an_op.value + rkm.b
            status = 0
        elif prob.status == cp.OPTIMAL_INACCURATE:
            print(prob.status)
            b = ap_op.value - an_op.value + rkm.b
            status = 4
        else:
            print(prob.status)
            print('Replaced b with original b')
            b = rkm.b
            status = 2


    return (status,l,b)
    


    
    


def RK_variable_b(rkm, dt, f, w0=[1.,0], t_final=1.,b_fixed = False,solver = cp.ECOS,
                 fallback = True,num_fallback = 1,maxval = None,dumpK=False,verbose=False,LP_opts = {}, 
                 return_status = False,dump_change = False):

    """    
    Options:
    
        rkm:        Base Runge-Kutta method, in Nodepy format
        dt:         time step size
        f:          RHS of ODE system
        w0:         Initial data
        t_final:    final solution time  
        b_fixed:    if True rkm.b are used
        solver:     the solver used for solving the LP Problem
        fallback:   if True the order of the Order Conditions is reduced by num_fallback
        maxval:     If scalar enforce the value as maximum on solution. If not used set to None
        dumpK:      if True the stage values are also returned
        dump_change:if True the change to the u_n at ecah step is also returned
        verbose:    if True function prints additional messages
        return_status: if True the function returns additional messages

    Returns:
        u:      Matrix with the solution
        t:      vector with the times
        b:      Matrix with the used b's

        if dumpK = True:
         K:     Array of the K Matrix containing the stagevalues
        if return_status
         status: dict containing
                'dt': the used dt
                'success': True if solver succeded, False if a inveasible or illdefined LP-Problem occured or the solver crashed
                'message': String containing more details
                'b':       Array with the indecies where b was changed
    """
    
    if not 'verbose_LP' in LP_opts.keys():
        LP_opts['verbose_LP'] = verbose


    #setup Variables for Soulution storage
    p = len(w0) #number of dimentions
    
    uu = np.zeros([p,int(t_final/dt)+100])
    uu[:,0] = w0.copy()
    tt = np.zeros([int(t_final/dt)+100])
    
    
    if dumpK:
        KK = ['null']
    if dump_change:
        NN = ['null'] #array for the norms

    
    
    #Setup Runge Kutta 
    c = rkm.c
    A = rkm.A #has to be lower left triangle
    s = len(c) #number of Stages
    K = np.zeros([p,s])
    
    u = np.array(w0)
    t = 0.
    n = 0
    
    
    if b_fixed == False:
        if fallback == True:
            p_new = max(rkm.p-num_fallback,1) #Order = 0 is not allowed
            O, rhs = OrderCond(rkm.A,rkm.c,order = p_new) #Fallback
        else:
            p_new = rkm.p
            O, rhs = OrderCond(rkm.A,rkm.c,order = rkm.p) 
        
        
        LP_vars = setup_LP(solver,rkm,O,rhs,s)
    else:
        p_new = None
          
        
    #for debugging b's    
    bb = np.zeros([s,int(t_final/dt)+2])
        
    status = {
        'basemethod': rkm.name,
        'order':{'orig':rkm.p,'new':p_new},
        'dt': dt,
        'success': True,
        'message': '',
        'neg_stage': {},
        'b':[],
        'change':[],
        'nup_pos_constriants':{},
        'maxval':maxval
    }
    
    if verbose: print('set up starting to solve')

    while t<t_final:
        for i in range(s):        #compute Stages

            u_prime = u.copy()
            for m in range(i):
                u_prime += dt*A[i,m]*K[:,m]
            
            K[:,i] = f(t+c[i]*dt,u_prime)
            
            if np.any(u_prime<-1.e-6):
                status['message'] = status['message'] + 'negative u\' at step'+ str(n+1) + 'stage' + str(n+1) + '\n'
                if n+1 not in status['neg_stage'].keys():
                    status['neg_stage'][n+1] = np.zeros(s)
                status['neg_stage'][n+1][i] = 1 #np.nonzero(u_prime > 1.e-6)
                if verbose: print(n+1,i,u_prime) #print input to f(t,u) if it is negative
            
        if dumpK:
            KK.append(K.copy())
        
        if b_fixed == False:
            #test if constraints are correct 
            u_ = u+dt*K@rkm.b 
            if (u_>= 0).all() and (not maxval or (u_<= maxval).all()):
                b =rkm.b
            
            else:
                 #Run Optimisation Problem
                if verbose: print('change b',n+1)
                status['b'].append(n+1)
                (status_LP,l,b) = solve_LP(solver,LP_vars,rkm,u,K,dt,maxval = maxval,**LP_opts)
                if status_LP in [1,4]:
                    status['message'] = status['message'] + 'LP-solver reported Problem:'+ str(status_LP)+ 'at step' + str(n+1) + '\n'
                elif status_LP in [2,3,5]: 
                     status['success'] = False
                     status['message'] = status['message'] + 'LP-solver failed at step '+ str(n+1) + '\n'
                     break
                status['change'].append(np.linalg.norm(K@(b-rkm.b)))
                status['nup_pos_constriants'][n]=l
            
        else:
            b =rkm.b
        #update
        u += dt*K@b 
        if dump_change:
            NN.append(np.linalg.norm(K@(b-rkm.b)))
        n += 1
        t += dt
        
        uu[:,n] = u.copy()
        bb[:,n] = b.copy()
        tt[n] = t
        #print('updated')


    ret = (tt[0:n+1],uu[:,0:n+1],bb[:,0:n+1])
    if dumpK:
        ret= ret + (KK,)
    if dump_change:
        ret= ret + (NN,)
    if return_status:
        ret= ret + (status,)
    
    return ret
        



        
        
